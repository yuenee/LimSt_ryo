package com.example.demo.week5.service;

import org.springframework.stereotype.Service;

@Service
public class CompoundInterestService {
	
    /**
     * <pre>
     * 【問題文】
     * getResultは引数のprincipal(元金)、rate(金利)、years(年数)を用いて、
     * 複利計算結果を取得する処理です。
     * ・複利を計算する処理を記載してください。
     * ・計算結果は変数resultに格納して返却してください。
     * </pre>
     *  @param principal 元金
     *  @param rate 金利
     *  @param years 年数
     *  @return result 複利計算結果
     */
	
	public double getResult(int principal,double rate,int years) {
		double interestRate = principal; 
		for (int i = 1; i <= years; i++) {
			interestRate = interestRate * (rate / 100 + 1);
		}
	    double result = interestRate;
	    return result;
	}

}
